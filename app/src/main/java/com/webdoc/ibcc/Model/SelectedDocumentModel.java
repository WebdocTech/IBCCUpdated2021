package com.webdoc.ibcc.Model;

public class SelectedDocumentModel {
    String title;
    int amount;

    public SelectedDocumentModel(String title, int amount) {
        this.title = title;
        this.amount = amount;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

}
